class Nodo {

    private int valorA;
    private int valorB;

    public Nodo() {
    }

    public Nodo(int valorA, int valorB) {
        this.valorA = valorA;
        this.valorB = valorB;
    }

    public int getValorA() {
        return valorA;
    }

    public void setValorA(int valorA) {
        this.valorA = valorA;
    }

    public int getValorB() {
        return valorB;
    }

    public void setValorB(int valorB) {
        this.valorB = valorB;
    }

}
